public class PerulanganFor {
    public static void main(String[] args) {
        // for ever loop
        for(;;) {
            // do something
            break;
        }

        // conditional
        int c = 1;
        for(; c <= 10;) {
            // do something
            c++;
        }

        // init
        for (int d = 1; d <=10; d++) {
            // do something
        }

    }
}
